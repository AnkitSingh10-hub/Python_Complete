# List sorting

thislist = ["orange", "mango", "kiwi", "pineapple", "banana"]
thislist.sort()
print(thislist, "\n")

thislist = [100, 50, 65, 82, 23]
thislist.sort()
print(thislist, "\n")

thislist = [100, 50, 65, 82, 23]
thislist.sort(reverse=True)
print(thislist, "\n")

thislist = ["banana", "Orange", "Kiwi", "cherry"]
thislist.reverse()
print(thislist, "\n")

# copy lists
thislist = ["apple", "banana", "cherry"]
mylist = thislist.copy()
print(mylist, "\n")

# or just assign to a variable

a = mylist
print(a)
